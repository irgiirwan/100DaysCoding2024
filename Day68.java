package SeratusHariJava;

public class Day68 {
    // Method Non-void
    
    public static int luasPersegi(int sisi) { 
        int luasPersegi = sisi + sisi; 
        return luasPersegi; 
    } 
    
    public static void main(String[] args) { 
        int hasil = luasPersegi(5);
        System.out.println("Maka, luas persegi adalah: "+ hasil); 
    } 
}
